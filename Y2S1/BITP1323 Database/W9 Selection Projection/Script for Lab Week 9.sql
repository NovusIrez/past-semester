DROP TABLE ASSIGNMENT_PROJ CASCADE CONSTRAINTS;
DROP TABLE PROJECT_EMPLOYEE CASCADE CONSTRAINTS;
DROP TABLE PROJECT CASCADE CONSTRAINTS;

CREATE TABLE PROJECT(
	ProjectID Number Not Null,
	Name Char(25) Unique Not Null,
	Department Varchar2(100) Null,
	MaxHours Number(6,1) Default 100,
	CONSTRAINT ProjectPK Primary Key (ProjectID)
);


CREATE TABLE PROJECT_EMPLOYEE(
	EmployeeNumber Number Not Null,
	Name Char(25) Not Null,
	Phone Char(8),
	Department Varchar(100),
	CONSTRAINT Project_EmployeePK Primary Key (EmployeeNumber)
);

CREATE TABLE ASSIGNMENT_PROJ (
	ProjectID Number Not Null,
	EmployeeNum Number Not Null,
	HoursWorked DOUBLE PRECISION Default 10,
	CONSTRAINT AssignmentPK Primary Key (ProjectID, EmployeeNum),
	CONSTRAINT ProjectFK Foreign Key(ProjectID) References PROJECT(ProjectID) ON DELETE CASCADE,
	CONSTRAINT ProjectEmployeeFK Foreign Key(EmployeeNum) References PROJECT_EMPLOYEE(EmployeeNumber)
	
);

